package com.company.lessons_average.Search_multiple_values;

class Points {

    String name;
    String address;
    int phone;
    int coord1;
    int coord2;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }

    public int getCoord1() {
        return coord1;
    }

    public void setCoord1(int coord1) {
        this.coord1 = coord1;
    }

    public int getCoord2() {
        return coord2;
    }

    public void setCoord2(int coord2) {
        this.coord2 = coord2;
    }
}