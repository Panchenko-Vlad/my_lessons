package HeadFirst.Command_6.Remote_2.Commands.Hottub;

import HeadFirst.Command_6.Remote_2.Commands.Command;

public class HottubOnCommand implements Command {
	Hottub hottub;

	public HottubOnCommand(Hottub hottub) {
		this.hottub = hottub;
	}

	public void execute() {
		hottub.on();
		hottub.heat();
		hottub.bubblesOn();
	}
}
