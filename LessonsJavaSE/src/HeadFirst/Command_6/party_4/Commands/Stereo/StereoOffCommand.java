package HeadFirst.Command_6.Party_4.Commands.Stereo;

import HeadFirst.Command_6.Party_4.Commands.Command;

public class StereoOffCommand implements Command {
	Stereo stereo;
 
	public StereoOffCommand(Stereo stereo) {
		this.stereo = stereo;
	}
 
	public void execute() {
		stereo.off();
	}

	public void undo() {
		stereo.on();
	}
}
