package PRACTICAL_WORKS2.Linzu;

public class UvelichitelnoeSteklo {

    private double maxUvelichenie;
    private int size; // см

    public UvelichitelnoeSteklo(double maxUvelichenie, int size) {
        this.maxUvelichenie = maxUvelichenie;
        this.size = size;
    }

    public double getMaxUvelichenie() {
        return maxUvelichenie;
    }

    public int getSize() {
        return size;
    }
}
